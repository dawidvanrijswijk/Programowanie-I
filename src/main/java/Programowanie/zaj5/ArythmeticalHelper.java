public class ArythmeticalHelper {

    public static Double sum(double a, double b) {
        return a + b;
    }

    public static Double mult(double a, double b) {
        return a * b;
    }

    public static Double maxAbs(double a, double b) {return Math.max(Math.abs(a), Math.abs(b));
    }
}